package model;

public class HavaOngoru {
 	private String sehirAdi;
 	private String havaCode;
 	private String ulke;
 	private String havaControl;
	public String getSehirAdi() {
		return sehirAdi;
	}
	public void setSehirAdi(String sehirAdi) {
		this.sehirAdi = sehirAdi;
	}
	public String getHavaCode() {
		return havaCode;
	}
	public void setHavaCode(String havaCode) {
		this.havaCode = havaCode;
	}
	public String getUlke() {
		return ulke;
	}
	public void setUlke(String ulke) {
		this.ulke = ulke;
	}
	public String getHavaControl() {
		return havaControl;
	}
	public void setHavaControl(String havaControl) {
		this.havaControl = havaControl;
	}
 	
 	
 	
}
